 
//Arithmetic Operator
 
#include<iostream>
 
using namespace std;
 
int main()
 
{
 
    int a,b,c;
 
    cout<<"Enter two Integer values:";
 
    cin>>a>>b;
 
    c=a+b;
 
    cout<<"\nAddition="<<c;
 
    c=a-b;
 
    cout<<"\nSubtraction="<<c;
 
    c=a*b;
 
    cout<<"\nMultiplication="<<c;
 
    c=a/b;
 
    cout<<"\nDivision="<<c;
 
    c=a%b;
 
    cout<<"\nRemainder="<<c;
 
    return 0;
 
}
 
