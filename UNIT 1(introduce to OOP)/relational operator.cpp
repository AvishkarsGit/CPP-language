 
//Relational Operator
 
#include<iostream>
 
using namespace std;
 
int main()
 
{
 
    cout<<"\n10<5   =>"<<(10<5);
 
    cout<<"\n10>5   =>"<<(10>5);
 
    cout<<"\n10<=5  =>"<<(10<=5);
 
    cout<<"\n10>=5  =>"<<(10>=5);
 
    cout<<"\n10==10 =>"<<(10==10);
 
    cout<<"\n10!=10 =>"<<(10!=10);
 
    return 0;
 
}
 
